n = float(input ("Digite um numero: "))
dobro = n*2
triplo = n*3
quadrado = n**2
raiz = n**(1/2)
raiz_cubica= n**(1/3)

print(f"O numero digitado foi: {n:.0f}")
print(f"O dobro é: {dobro}")
print(f"O triplo é: {triplo}")
print(f"O quadrodo é: {quadrado}")
print(f"A raiz é: {raiz}")
print(f"A raiz cubica é: {raiz_cubica:.2f}")
